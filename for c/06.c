// Addition, Subtraction, Multiplication, Division
#include<stdio.h>

int main()
{
    int a = 20;
    int b = 5;

    int add, sub, mul, div;

    add = a + b;
    sub = a - b;
    mul = a * b;
    div = a / b;

    printf("Addition = %d\n", add);
    printf("Subtraction = %d\n", sub);
    printf("Multiplication = %d\n", mul);
    printf("Division = %d\n", div);

    return 0;
}